					  A dress watch is meant to accompany a gentleman on formal occasions, and compliment a suit and tie. Be either a business meeting, a fancy date or a wedding, dress watches are classy, elegant, and straight to the point. No fancy complications nor functions, just plain style. They also tend to be rather smaller timepieces, and in the past their size used to vary between 34 and 38 mm.</br>

Examples: Fonderia Navale Veneto, Timex Marlin, Nomos Orion

