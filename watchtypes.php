					  The original field watches were the ones used in both world wars, and were expected to perform under the pressure of combat. Their key features were ruggedness, decent waterproof rating, luminescence and clear, easy to read dial. Modern incarnations are inspired on this past designs, and evoke a military aesthetic. They usually come in a NATO strap, as it became a standard in the army.</br>

Examples: Hamilton Khaki, Timex Mk1, Weiss Standard Field Issue

