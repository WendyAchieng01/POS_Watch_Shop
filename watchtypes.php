                      <p class = "about_table_paragraphs">
					  In general, a dive or diver’s watch is any watch with 200+ water resistance rating. They were originally meant to accompany divers on their trips deep below the sea. Since they were used on top of a diving suit, they used to be bigger, and needed to be easily read even in an extremely dark environment. They also featured a rotating bezel. This was meant to be able to countdown and tell the diver how much oxygen was left on the tank, translated in minutes. A diver would set the bezel before diving, then check again afterwards and read “i’ve got 10 minutes left before oxygen runs out”. Since they needed to be in constant contact with seawater and seawater corrosion, diver watches demanded high quality materials, usually high grade stainless steel, or a copper alloy such as bronze or brass.</br>

Examples of diver watches: Pontvs Nessi, Rolex Submariner, Vostok Amphibia</br>
However, the diver style became extremely popular after Sean Connery’s James Bond used it with a Nato strap on Dr. No. Since then the Rolex Submariner has easily become one of the most recognizable watch designs, and “diver watch” a synonym of similar watches.

